def add(a,b):
    return a+b
def sub(a,b):
    return a-b
def mul(a,b):
    return a*b
def div(a,b):
    return a//b
x=int(input("Enter 1st number:"))
y=int(input("Enter 2nd number:"))
print("Addition:",add(x,y))
print("Subtraction:",sub(x,y))
print("Multiplication:",mul(x,y))
print("Division:",div(x,y))